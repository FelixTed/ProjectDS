package finalProjectDS;

public class Instructor extends Person{
	//data fields
	private String officeNumber;
	
	//default Constructor
	public Instructor(){
		
	}
	
	//constructor for all data fields and uses Person's constructor
	public Instructor(String officeNumber, String name, String familyName) {
		super(name, familyName);
		this.officeNumber = officeNumber;
	}
	
	//returns officeNumber as a string
	public String getOfficeNumber() {
		return this.officeNumber;
	}
	
	//sets officeNumber to desired string
	public void setOfficeNumber(String officeNumber) {
		this.officeNumber = officeNumber;
	}
	
	//Returns class information as a string
	public String toString() {
		return "Instructor name: " + this.getName() + " " + this.getFamilyName() 
			+" Office number: " + this.officeNumber;
	}
}


























































































































































































































































































































































































































































































































































































































































































































































































































































